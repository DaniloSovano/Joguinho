
public class Player {
  public String nome;
  private int HP;
  private int atackForce;
  private int resistência;
  private int defesa;
  private int destreza;
  private int velocidade;


  public int getAtackForce() {
    return atackForce;
  }
  public void setAtackForce(int atackForce) {
    this.atackForce = atackForce;
  }

  public int getResistência() {
    return resistência;
  }
  public void setResistência(int resistência) {
    this.resistência = resistência;
  }

  public int getDefesa() {
    return defesa;
  }
  public void setDefesa(int defesa) {
    this.defesa = defesa;
  }

  public int getDestreza() {
    return destreza;
  }
  public void setDestreza(int destreza) {
    this.destreza = destreza;
  }

  public int getVelocidade() {
    return velocidade;
  }
  public void setVelocidade(int velocidade) {
    this.velocidade = velocidade;
  }

  public int getHP() {
    return HP;
  }
  public void setHP(int HP) {
    this.HP = HP;
  }


}
